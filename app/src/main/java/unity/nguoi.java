package unity;

public class nguoi {
    String hoten;
    String anh;

    public nguoi(String hoten, String anh) {
        this.hoten = hoten;
        this.anh = anh;
    }

    public String getHoten() {
        return hoten;
    }

    public void setHoten(String hoten) {
        this.hoten = hoten;
    }

    public String getAnh() {
        return anh;
    }

    public void setAnh(String anh) {
        this.anh = anh;
    }
}
